    public static double calculateAttendance(int attendance){
        double attendanceGrade;
        attendanceGrade = switch (attendance) {
            case 12 -> 15;
            case 11 -> 12;
            case 10 -> 9;
            case 9 -> 6;
            case 8 -> 3;
            case 7 -> 1;
            default -> 0;
        };
        return attendanceGrade;
    }
    
    public static double calculateGradePoint(double finalGrades){
        double average;
        if (finalGrades >= 96){
            average = 1.00;
        } else if (finalGrades >= 91.5){
            average = 1.25;
        } else if (finalGrades >= 87.01){
            average = 1.50;
        } else if (finalGrades >= 82.51){
            average = 1.75;
        } else if (finalGrades >= 78.01){
            average = 2.0;
        } else if (finalGrades >= 73.51){
            average = 2.25;
        } else if (finalGrades >= 69.01){
            average = 2.50;
        } else if (finalGrades >= 64.51){
            average = 2.75;
        } else if (finalGrades >= 60.00){
            average = 3.0;
        } else{
            average = 5.0;
        }
                return average;
    }
