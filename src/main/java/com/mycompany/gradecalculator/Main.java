    private void buttonCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCalculateActionPerformed
        try {
            double ms1Initial = Double.parseDouble(ms1Score.getText());
            double ms1Final = Double.parseDouble(ms1TotalScore.getText());
            double ms2Initial = Double.parseDouble(ms2Score.getText());
            double ms2Final = Double.parseDouble(ms2TotalScore.getText());
            double taInitial = Double.parseDouble(taScore.getText());
            double taFinal = Double.parseDouble(taTotalScore.getText());
            int attendance = Integer.parseInt(present.getText());

            // Validate inputs
            if (ms1Score.getText().isEmpty() || ms1TotalScore.getText().isEmpty() ||
                ms2Score.getText().isEmpty() || ms2TotalScore.getText().isEmpty() ||
                taScore.getText().isEmpty() || taTotalScore.getText().isEmpty() ||
                present.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Validate numeric inputs
            if (ms1Initial < 0 || ms1Final <= 0 || ms2Initial < 0 || ms2Final <= 0 || taInitial < 0 || taFinal <= 0 || attendance < 0 || attendance > MAXIMUM_DAY) {
                JOptionPane.showMessageDialog(this, "Invalid input. Please enter valid positive integers.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Validate milestone and terminal assessment ranges
            if (ms1Initial > ms1Final || ms2Initial > ms2Final || taInitial > taFinal) {
                JOptionPane.showMessageDialog(this, "Initial score must be less than or equal to final score.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Validate attendance days
            if (attendance > MAXIMUM_DAY) {
                JOptionPane.showMessageDialog(this, "Attendance must not exceed 12 weeks.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Calculate percentages and grade points
            double mileStone1FinalScore = (ms1Initial / ms1Final) * MAXIMUM_MILESTONE_GRADE;
            double mileStone2FinalScore = (ms2Initial / ms2Final) * MAXIMUM_MILESTONE_GRADE;
            double terminalAssessmentFinalScore = (taInitial / taFinal) * MAXIMUM_TERMINAL_ASSESSMENT_GRADE;

            double initialGrades = mileStone1FinalScore + mileStone2FinalScore + terminalAssessmentFinalScore;
            double finalGrades = initialGrades + calculateAttendance(attendance);
            double gradePoint = calculateGradePoint(finalGrades);

            // Display results
            ms1Field.setText(String.format("%.2f%%", mileStone1FinalScore));
            ms2Field.setText(String.format("%.2f%%", mileStone2FinalScore));
            taField.setText(String.format("%.2f%%", terminalAssessmentFinalScore));
            attendanceField.setText(String.format("%.2f%%", calculateAttendance(attendance)));
            percentageField.setText(String.format("%.2f%%", finalGrades));
            gradePointsField.setText(String.format("%.2f", gradePoint));

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid input. Please enter valid integers.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_buttonCalculateActionPerformed

